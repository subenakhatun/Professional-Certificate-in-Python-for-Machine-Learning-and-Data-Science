# NumPy - Statistical Functions: np.mean(arr)/median(arr)/std(arr)/var(arr)
import numpy as np

# Creating an array
array_1 = np.array([1, 2, 3, 4, 5])
print(f'Mean of array: {np.mean(array_1)}')
print(f'Median of array: {np.median(array_1)}')
print(f'Standard Deviation of array: {np.std(array_1)}')
print(f'Varience of array: {np.var(array_1)}')
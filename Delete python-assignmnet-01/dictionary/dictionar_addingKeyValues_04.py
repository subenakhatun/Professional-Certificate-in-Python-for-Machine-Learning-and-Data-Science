# Example 4: Adding Key-Value Pairs to a Dictionary
# You can add new key-value pairs to a dictionary by assigning a value to a new key.


my_dic = {
    'name':['Subena','Shuly','Rahena','Farzana','Dhoha'],
    'Subjects':['Future Data Scientist','Home maker','Dreamer','Hridoyban','just beginning her life'],
    'Marks':[70,80,90,100,100],
    'Age':[30,35,31,32,18],
    'City':['Dhaka','Sylhet','Sylhet','Sylhet','Dhaka']       
}

# Adding a new key-value pair
my_dic['city'] = 'New York'

# Printing the updated dictionary
print('Updated Dictionary:', my_dic)
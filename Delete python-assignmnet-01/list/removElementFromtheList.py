# You can remove elements from a list using remove() and pop(). using postion no
# remove() deletes the first occurrence of a value, while pop() removes an element by index.

my_list = [34,40,55,80,96,35,70,29,85,49,70]
# remove an element
my_list.remove(70)

# popped element 
my_list.pop(3)

#  list after remove and pop
print(my_list)
